    // OnStateExit is called when a transition ends and the state machine finishes evaluating this state
    override public void OnStateExit(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        if (HasParameter("isAttackLong", animator))
        {
            animator.SetBool("isAttackLong", false);
        }
        if (HasParameter("isAttackShort", animator))
        {
            animator.SetBool("isAttackShort", false);
        }
        if (HasParameter("isAttack", animator))
        {
            animator.SetBool("isAttack", false);
        }
        if (HasParameter("isAttacked", animator))
        {
            animator.SetBool("isAttacked", false);
        }
    }

    private bool HasParameter(string paramName, Animator animator)
    {
        foreach (AnimatorControllerParameter param in animator.parameters)
        {
            if (param.name == paramName)
                return true;
        }
        return false;
    }

